function detectUnit(number) {
  if (number >= 1 && number <= 50) return "Unit 3";
  if (number >= 51 && number <= 98) return "Unit 4";
  return "Unit 5";
}

function parseQuestions(raw) {
  const normalized = raw.replace(/\r/g, "").trim();
  const blocks = normalized.split(/\n(?=Q\d+\.)/g);
  const questions = [];

  for (const block of blocks) {
    const trimmed = block.trim();
    if (!/^Q\d+\./.test(trimmed)) continue;

    const qMatch = trimmed.match(/^Q(\d+)\.\s*([\s\S]*?)\nA\)\s*([\s\S]*?)\nB\)\s*([\s\S]*?)\nC\)\s*([\s\S]*?)\nD\)\s*([\s\S]*?)\nAnswer:\s*([A-D])\s*\nExplanation:\s*([\s\S]*)$/);
    if (!qMatch) continue;

    const qNumber = Number(qMatch[1]);
    const questionText = qMatch[2].trim();
    const options = [
      qMatch[3].trim(),
      qMatch[4].trim(),
      qMatch[5].trim(),
      qMatch[6].trim()
    ];
    const answerLetter = qMatch[7].trim();
    const explanation = qMatch[8].trim();

    questions.push({
      originalNumber: qNumber,
      unit: detectUnit(qNumber),
      question: questionText,
      options,
      answerIndex: "ABCD".indexOf(answerLetter),
      answerLetter,
      explanation
    });
  }

  return questions;
}

function getStats() {
  let answered = 0;
  let score = 0;

  for (const value of answerState.values()) {
    answered++;
    if (value.correct) score++;
  }

  return { answered, score };
}

function updateStats() {
  const stats = getStats();
  totalCount.textContent = allQuestions.length;
  answeredCount.textContent = stats.answered;
  scoreCount.textContent = stats.score;
  showingText.textContent = `Showing ${filteredQuestions.length} of ${allQuestions.length} questions`;
}

function renderQuestions() {
  quizContainer.innerHTML = "";

  if (!filteredQuestions.length) {
    quizContainer.innerHTML = `
      <div class="empty-state">
        No questions found. Try another search or unit filter.
      </div>
    `;
    updateStats();
    return;
  }

  filteredQuestions.forEach((item) => {
    const saved = answerState.get(item.originalNumber);
    const card = document.createElement("div");
    card.className = "question-card";

    const optionsHtml = item.options
      .map((option, index) => {
        let classes = "option-btn";
        let disabled = "";

        if (saved) {
          disabled = "disabled";
          if (index === item.answerIndex) classes += " correct";
          if (saved.selectedIndex === index && !saved.correct) classes += " wrong";
        }

        return `
          <button class="${classes}" data-q="${item.originalNumber}" data-index="${index}" ${disabled}>
            <strong>${String.fromCharCode(65 + index)}.</strong> ${option}
          </button>
        `;
      })
      .join("");

    const feedbackVisible = saved ? "show" : "";
    const feedbackTitleClass = saved?.correct ? "correct-text" : "wrong-text";
    const feedbackTitle = saved
      ? saved.correct
        ? "Correct answer"
        : `Wrong answer. Correct option is ${item.answerLetter}.`
      : "";

    card.innerHTML = `
      <div class="question-top">
        <span class="badge">${item.unit}</span>
        <span class="badge">Q${item.originalNumber}</span>
      </div>

      <p class="question-text">${item.question}</p>

      <div class="options">
        ${optionsHtml}
      </div>

      <div class="feedback-box ${feedbackVisible}">
        <div class="feedback-title ${feedbackTitleClass}">${feedbackTitle}</div>
        <p class="feedback-explanation">${saved ? item.explanation : ""}</p>
      </div>
    `;

    quizContainer.appendChild(card);
  });

  attachOptionHandlers();
  updateStats();
}

function attachOptionHandlers() {
  const buttons = document.querySelectorAll(".option-btn");

  buttons.forEach((button) => {
    button.addEventListener("click", () => {
      const qNo = Number(button.dataset.q);
      const selectedIndex = Number(button.dataset.index);

      if (answerState.has(qNo)) return;

      const question = allQuestions.find((q) => q.originalNumber === qNo);
      const correct = selectedIndex === question.answerIndex;

      answerState.set(qNo, {
        selectedIndex,
        correct
      });

      renderQuestions();
    });
  });
}

function applyFilters() {
  const keyword = searchInput.value.trim().toLowerCase();
  const selectedUnit = unitFilter.value;

  filteredQuestions = allQuestions.filter((item) => {
    const unitMatch = selectedUnit === "all" || item.unit === selectedUnit;

    const haystack = [
      item.question,
      ...item.options,
      item.answerLetter,
      item.explanation,
      item.unit,
      `q${item.originalNumber}`
    ]
      .join(" ")
      .toLowerCase();

    const textMatch = !keyword || haystack.includes(keyword);
    return unitMatch && textMatch;
  });

  renderQuestions();
}

function shuffleQuestions() {
  const current = [...filteredQuestions];
  for (let i = current.length - 1; i > 0; i--) {
    const j = Math.floor(Math.random() * (i + 1));
    [current[i], current[j]] = [current[j], current[i]];
  }
  filteredQuestions = current;
  renderQuestions();
}

